package Aula3PT1;

class Calculadora {

  public double adicao(double n1, double n2) {
    return n1 + n2;
  }

  public double subtracao(double n1, double n2) {
    return n1 - n2;
  }

  public double multiplicacao(double n1, double n2) {
    return n1 * n2;
  }

  public double divisao(double n1, double n2) {
    return n1 / n2;
  }
}
